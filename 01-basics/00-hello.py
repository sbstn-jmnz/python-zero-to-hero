# Los lenguajes de programación como Python, los podemos pensar como una simple calculadora. Tienen valores como los números y también tienen operadores como los clásicos +, -, *, /. Pero además de solo números, la mayoría de los lenguajes modernos como Python, permite también manipular textos, y otros tipos de valores para representar colores, imágenes, sonidos, etc. Pero en su su esencia, no soy tan diferentes a una simple calculadora.

# Python además de funcionar directamente como un programa de la terminal, lo podemos utilizar en conjunto con archivos con sentencias de Python. Esto es, en otras palabras, utilizando la sintaxis de Python.

2 + 4 

# En el caso anterior, Python efectivamente ejecutó esa operación o computo. Pero no muestra automáticamente el resultado de cada sentencia. 
# Si queremos o necesitamos mostrar el resultado por la terminal, debemos indicarlo explícitamente utilizando una función incorporada para estos efectos.

print(2 + 4)

# Print manda el resultado a la salida estándar, que para nuestros efectos es precisamente la terminal.


